# Pomocné mapy pro rychlé vyhledávání
TAXON_BY_LATIN = {t['la']: t for t in TAXONS}


def find_taxon(latin_name: str) -> dict | None:
    """Najde taxon dle latinského názvu."""
    return TAXON_BY_LATIN.get(latin_name)


def is_coniferous(latin_name: str) -> bool:
    """Určí, zda jde o jehličnan (dle tvaru koruny v Tab. 1 - kuželovitá = jehličnan).

    Pro kompenzační účely se používá i jiná klasifikace dle skupin (Tab. 14).
    """
    coniferous_genera = (
        'Abies', 'Calocedrus', 'Cedrus', 'Chamaecyparis', 'Cryptomeria',
        'Cupressocyparis', 'Ginkgo', 'Juniperus', 'Larix', 'Metasequoia',
        'Picea', 'Pinus', 'Pseudolarix', 'Pseudotsuga', 'Sciadopitys',
        'Sequoiadendron', 'Taxodium', 'Taxus', 'Thuja', 'Thujopsis',
        'Torreya', 'Tsuga', 'Cephalotaxus',
    )
    first_word = latin_name.split()[0] if latin_name else ''
    return first_word in coniferous_genera
