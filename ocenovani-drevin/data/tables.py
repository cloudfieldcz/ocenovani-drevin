def get_real_crown_volume(crown_height_m: float, crown_diameter_m: float, is_coniferous: bool) -> float:
    """Vrátí skutečný objem koruny interpolací/zaokrouhlením.

    crown_height_m: rozdíl výšky stromu a výšky nasazení koruny
    crown_diameter_m: průměr koruny
    """
    table = TAB_3B_CONIFEROUS if is_coniferous else TAB_3A_DECIDUOUS

    # Zaokrouhlit na celá čísla a omezit do rozsahu tabulky
    h = max(3, min(33, round(crown_height_m)))
    d = max(3, min(25, round(crown_diameter_m)))

    return float(table[h][d])


