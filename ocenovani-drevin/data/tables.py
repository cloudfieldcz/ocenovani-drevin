# =============================================================================
# Inflační koeficient (CPI od 2020 do aktuálního roku)
# Hodnoty z ČSÚ - průměrná roční míra inflace
# Metodika: od roku 2023 se používá nejnovější inflační koeficient
# dostupný k 1.1. daného roku, kumulativně od cenové úrovně 2020.
# =============================================================================
ANNUAL_INFLATION = {
    # rok -> průměrná roční míra inflace (%)
    2021: 3.8,
    2022: 15.1,
    2023: 10.7,
    2024: 2.4,
    2025: 2.5,   # odhad - aktualizovat dle ČSÚ
}


def get_inflation_coefficient(target_year: int) -> float:
    """Vrátí kumulativní inflační koeficient od ceny 2020 do target_year.

    Dle metodiky: hodnoty 2021 a 2022 = 1.0 (cenová úroveň 2020).
    Od 2023 se aplikují všechny dostupné CPI roky k 1.1.
    Pro rok N >= 2023 se používají CPI roky 2021..(N-2).
    """
    if target_year <= 2022:
        return 1.0
    coef = 1.0
    # K 1.1. roku N je dostupné CPI za rok (N-1)? Ne, dle metodiky se pro 2023 použije 2021.
    # Pro 2024 by se použily 2021 a 2022, atd. Tj. roky 2021..(target_year-2).
    for y in range(2021, target_year - 1):
        if y in ANNUAL_INFLATION:
            coef *= 1.0 + ANNUAL_INFLATION[y] / 100.0
    return round(coef, 4)
